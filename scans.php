									<h3 class="h3-md steelblue-color">The 20-23 Week Scan (Fetal Anomaly Scan)</h3>

									<!-- Text -->
									<p style="text-align:justify">This scan is performed transabdominally. It will confirm that the vast majority of pregnancies are developing normally. However, about 1% of all fetuses will have a serious congenital abnormality and ultrasound examination at this stage will detect about 70% of these. In addition, screening tests for the later development of high blood pressure (pre-eclampsia), placental insufficiency, poor fetal growth or intrauterine growth retardation (IUGR) and premature labor can be performed.

Detailed inspection of every organ and part of the fetal anatomy is performed, including the fetal brain, spine, heart, lungs, kidneys, bowel and bladder, limbs, fingers and toes.
									</p>

									<!-- Image -->
									

									<!-- Text -->
									<p style="text-align:justify">These conditions are caused by poor inversion of the placenta into the wall of the uterus. Prediction of the possible development of these problems can be made by performing Doppler examination of blood flow in the uterine arteries.About 50% of cases of spontaneous delivery before 34 weeks are associated with shortening of the cervix. Ultrasound assessment of the cervix can be performed at this assessment, usually transvaginally.Although rare, we advocate screening for this abnormality. If your pregnancy is at increased risk or sub-optimal, views are obtained on ultrasound for technical reasons, we would recommend detailed cardiac scanning by a fetal cardiologist.


									</p>

