								<p style="text-align:justify">When a fertilized egg implants and grows outside the main cavity of the uterus it is termed as Ectopic Pregnancy. It often occurs in fallopian tubes, which carries the egg from the ovaries to the uterus. This type of ectopic pregnancy is called a tubal pregnancy.</p>
								<p style="text-align:justify">An ectopic pregnancy can’t proceed normally as the fertilized egg can’t survive, and the growing tissue may cause life-threatening bleeding if left untreated.</p>
								<h2>WHAT ARE THE CAUSES OF ECTOPIC PREGNANCY?</h2>
								<p style="text-align:justify">An ectopic pregnancy occurs when a fertilized egg gets stuck on its way to the uterus because of the damaged fallopian tube due to inflammation.</p>
