								<h3>Antenatal Care</h3>
								<p style="text-align:justify">Antenatal Care (ANC): Because, a little extra care can go a long way!</p>
								<p style="text-align:justify">Whether you are in good health or are coping with a rough pregnancy, staying fit and healthy is essential. This is where Antenatal Care (ANC) steps in</p>
								<h4>What is Antenatal Care?</h4>
								<p style="text-align:justify">Antenatal care or ANC is your first contact with the health care system when you are expecting. ANC is a healthcare service which is beyond maternity care. It includes diagnosing and managing any pregnancy-related complications and classes that will help you and your partner experience a happy, healthy pregnancy. The World Health Organization has recommended a minimum of 8 ANC visits when you are expecting.</p>
								<p style="text-align:justify">A high quality prenatal or Antenatal Care (ANC) not just helps you treat chronic conditions and non-communicable diseases when you are pregnant, but the classes involved in it can educate you and your better half on birth, breastfeeding, delivery, and parenting etc.</p>
								<p style="text-align:justify">From minimizing the risk of mother-to-child disease transmission (HIV) to encouraging the use of a skilled birth attendant, ANC is womb-like care that caters to all your needs.</p>
								<p style="text-align:justify"><strong>The Antenatal Care you go through depends on:</strong>
								</p>
								<ul>
									<li>The stage of pregnancy you are in</li>
									<li>Problems that you are experiencing (if any) during your pregnancy</li>
									<li>Your health condition and the risks you or your baby may have</li>
								</ul>
								<h4>What to expect during the appointments?</h4>
								<p style="text-align:justify">From the first appointment to the last session, there will be a series of tests, treatments, scans, check-ups, and discussions related to your health and your baby’s health.</p>
								<p style="text-align:justify">The discussions in ANC involves morning sickness (how to deal with it?), the medication you are taking and its effect on health (if any), the importance of a healthy lifestyle for you and your partner, and an intense discussion of your medical history and your previous pregnancies to avoid any complications. The healthcare provider also keeps a track on your pregnancy trimester and educates you on your due date and if in case of a tough pregnancy they educate you and your partner on the signs of a healthy pregnancy and any complications related to it.</p>
								<p style="text-align:justify">During the discussions, they provide you with a healthy diet plan and birth plan along with tests such as blood and urine tests, genetic screening, Pap test, ultrasounds, and others depending on your need. They keep a tab on your weight, pressure and the size of the growing tummy to predict the growth and development of your baby.</p>
								<p style="text-align:justify">The appointment also includes monitoring your baby’s heart rate to check how he/she is doing and counselling sessions to support you emotionally if you are depressed or anxious because of the constant fluctuating hormones. ANC suggests you take the antenatal classes for a better pregnancy experience.</p>
								<h4>What do you learn from the Antenatal classes?</h4>
								<p style="text-align:justify">Antenatal classes are a continuum of care for you and your little one. Given below are a few things that antenatal classes can enlighten you with:</p>
								<ul>
									<li>The healthcare provider prepares you for the labor (what to expect) and educates you on the different stages of labor, its signs and teaches you relaxation and breathing skills to use during the labor</li>
									<li>The healthcare specialist teaches you the methods of breastfeeding and skills on early parenting.</li>
									<li>They provide you with all the answers to the questions that have been running through your mind like when to call the hospital? What are the different birthing positions? What is the new-born baby’s behavior? Etc.</li>
								</ul>
