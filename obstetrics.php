								<h3>Pre-Pregnancy check up</h3>
								<h4>Why pre pregnancy check up?</h4>
								<p style="text-align:justify">Couple planning pregnancy have plenty of unanswered queries .Your doctor may run tests to make sure that you and your partner don't have any hidden illnesses that could affect your pregnancy or your chances of becoming pregnant. Your doctor can also give you advice about exercise, eating, lifestyle and folic acid supplement, Vitamin D supplements and advice on vaccines .Also She might check for any viral infections, genetic disorders in the family, and other medical conditions like PCOS, thyroid disorders , diabetes, hypertension which could potentially affect planning pregnancy</p>
								<p style="text-align:justify">Prior to conception, you and your doctor will need to discuss all medications you are taking, including any over the counter medications.</p>
								
								<p style="text-align:justify">If it's time for you to update your vaccines, it's important to do so before you are pregnant. A few specific vaccinations, such as the MMR (measles-mumps-rubella), varicella (the virus that causes chickenpox), or Hepatitis A vaccines increase the risk of birth defects. Experts advise that you wait at least 28 days after receiving some of these vaccinations before trying to conceive.</p>
								<p style="text-align:justify">Pre-pregnancy planning keeps you — and your future baby - as healthy as possible. Your doctor will talk with you about your:</p>
								<ul>
