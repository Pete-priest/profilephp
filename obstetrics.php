								<h3>Postnatal Care</h3>
								<p style="text-align:justify">As the roller coaster ride you hopped on comes to an end, there are miscellaneous things to be taken care of. The first few hours and days after delivery are the riskiest for both mothers and their newborns. That is why utter care should to given to both. While most people focus more on the pregnancy care on the nine months of pregnancy, postnatal care or postpartum care is important too. The postpartum period begins right after the delivery and lasts to about six to eight weeks.</p>
								<h2>What is Postnatal Care?</h2>
								<p style="text-align:justify">Postnatal care or PNC is specialized care for the mother that starts within an hour after the delivery and lasts through the following six to eight weeks. The care includes the prevention, elimination, early detection and cure of health complications (if any), counselling on breastfeeding, immunization, an interactive session on the importance of birth spacing and maternal nutrition, etc.</p>
								<p style="text-align:justify">After the delivery, the mother goes through many changes that can be physical and emotional while they learn to take care of their newborn. Therefore, postpartum or postnatal care involves getting proper rest, nutrition, and vaginal care for the mother for a quicker recovery.</p>
								<h2>Why is Postnatal Care important?</h2>
								<p style="text-align:justify">Postnatal care for women is essential after the delivery, keeping labor and birth in account. It is designed according to a woman’s individual needs and preferences and aims to create a supportive environment for the new mother and her baby where even the families are involved. Here, the families are guided by professionals to take care of the mother, and the baby and also the mothers are taught how to care for her baby and themselves.</p>
								<p style="text-align:justify">An essential element to postnatal care is midwifery care. If everything is well with the mother and the baby and depending on the type of birth, she is discharged home between 6-24 hours after delivery to the care of the community midwifery team. The midwives and/or the support workers are helping the mother adjust to her life with the new baby, helping her with feeding and baby care as well as giving her emotional support. A few of the postnatal midwifery services include:</p>
								<ul>
									<li>Assists the mother with breastfeeding</li>
									<li>Advice and supports the mother throughout the postnatal care</li>
									<li>Provides physical and emotional support to the new mother</li>
									<li>Will arrange for the full examination of baby, screening tests and hearing test if required</li>
								</ul>
								<p style="text-align:justify">Another important factor is the routine checkups after the delivery. Post delivery the woman is discharged to the care of the GP and Health Visitor by day 4-10 following birth if the emotional and physical condition of the other is stabilized. But it does not end here. A regular visit to the healthcare specialist is essential to rule out or treat any health-related issues. The healthcare specialist will formulate a healthcare plan according to the mother and her baby’s individual needs and these plans are communicated to all relevant professional groups that are involved in the mother and the baby’s care. You can trust Prolife for this. The Obstetric services at Prolife include:</p>
								<ul>
									<li>Antenatal care (before birth)</li>
									<li>Intrapartum care (during labor and delivery)</li>
									<li>Postnatal care (after birth)</li>
								</ul>
