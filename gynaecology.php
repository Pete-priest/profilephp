                                    <h3 class="h3-md steelblue-color">Ovarian Cysts</h3>
                                      
                  <h3></h3>
                          <p style="text-align:justify">A cyst is a fluid-filled sac that causes no pain or symptoms and sometimes develop on one of the ovaries. The ovaries are a part of the female reproductive system and are responsible for the production of eggs and hormones.</p>
<h2>Symptoms of ovarian cysts</h2>
<p style="text-align:justify">Cysts usually show no symptoms and are painless but sometimes as they grow in size, below symptoms may begin to appear:</p>
<ul>
<li>Pain while having intercourse</li>
<li>Tender breasts</li>
<li>Bloating of the abdomen</li>
<li>Painful bowel movements</li>
<li>Pelvic pain</li>
</ul>
<h2>Types of Ovarian Cysts</h2>
<p style="text-align:justify">There are various types of ovarian cysts of which functional cysts are the most common ones. These can be of two types, follicle and corpus luteum cysts.</p>
<ul>
<li><strong>Follicle Cyst:</strong> Every month, the egg is released from a sac called a follicle. The egg grows in this sac and the follicle breaks open to release it. But if the sac doesn’t open then the fluid inside it can develop into a cyst on the ovary.</li>
<li><strong>Corpus Luteum: </strong>These cysts develop in cases where the follicle sacs do not dissolve after releasing the egg. Additional fluid develops in the sac causing a cyst to form on the ovary.</li>
</ul>
<p style="text-align:justify">Other types of cysts include dermoid cysts that are growths containing hair or fat, endometriomas cysts that form due to growth of inner uterus tissues on the outside and cystadenomas cysts that develop on the outer surface of the ovaries.</p>
<h2>Diagnosis and Treatment of Cysts</h2>
<p style="text-align:justify">Ovarian cysts can be detected during a routine pelvic examination and your doctor may require you to get an ultrasound test done to locate the cyst and determine its size and shape. Since cysts usually disappear on their own after a few weeks, usually no immediate treatment is recommended. Instead, your doctor will ask you to come back in a few weeks for another ultrasound scan to check on the cyst.</p>
<p style="text-align:justify">In cases where the cyst does not disappear or starts to grow, treatment options like birth control pills to stop ovulation, laparoscopy or a laparotomy may be recommended by your doctor to remove the cyst.</p>

  

                    
