                                    <h3 class="h3-md steelblue-color"> Uterine & vaginal disorders</h3>
									
                  <h3></h3>
                          <h2>What is Uterine Prolapse?</h2>
<p style="text-align:justify">Uterine prolapse is defined as a condition which occurs when pelvic floor muscles and ligaments stretch and weaken and are not able to provide enough support for the uterus. It can happen in women of any age, but it often affects postmenopausal women who have had one or more vaginal deliveries.</p>
<h2>What causes Uterine Prolapse?</h2>
<p style="text-align:justify">It occurs due to the weak pelvic muscles and supportive tissues. Weak pelvic muscles and tissues can be caused due to:</p>
<ul>
<li>Pregnancy</li>
<li>Overweight</li>
<li>Chronic cough</li>
<li>Repeated heavy lifting</li>
<li>Difficult delivery and labor</li>
<li>Chronic constipation</li>
</ul>
<h2>What are the symptoms and risk factors of Uterine Prolapse?</h2>
<p style="text-align:justify">Mild uterine prolapse doesn’t show any signs or symptoms, but moderate to severe uterine prolapse have symptoms such as:</p>
<ul>
<li>The sensation of heaviness in the pelvis</li>
<li>Mass protruding from the vagina</li>
<li>Urinary problems</li>
<li>Trouble having a bowel movement</li>
<li>The sensation of looseness in the tone of the vaginal tissue</li>
</ul>
<h2>The risk factors of uterine prolapse include:</h2>
<ul>
<li>Obesity</li>
<li>Prior pelvic surgery</li>
<li>Chronic constipation</li>
<li>Delivering a large baby</li>
<li>One or more pregnancies and vaginal births</li>
<li>Increasing age</li>
</ul>
<h2>How is Uterine Prolapse diagnosed?</h2>
<p style="text-align:justify">Uterine prolapse is usually diagnosed during a pelvic exam. During the exam, the doctor will ask you to bear down as if having a bowel movement. This will help the doctor to assess how far the uterus has slipped into the vagina.</p>
<h2>What is the treatment for Uterine Prolapse?</h2>
<p style="text-align:justify">The treatment of uterine prolapse depends on the severity of the condition and symptoms. If there are no or very few symptoms, then the doctor will ask you to do Kegel exercises to strengthen the pelvic muscles, losing weight and treating constipation.</p>
<p style="text-align:justify">A vaginal pessary which is a plastic or rubber ring is inserted into the vagina to support the bulging tissues.</p>
<p style="text-align:justify">If the uterine prolapse is severe, then the doctor may recommend minimally invasive or vaginal surgery. Surgery can involve repair of weekend pelvic floor tissues or the removal of the uterus. Doctor recommends a hysterectomy if the condition is severe, but it possess long-term health risks such as increased risk of heart and blood vessel diseases.</p>


   
