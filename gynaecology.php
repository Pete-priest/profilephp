                                    <h2>What is Chronic Pelvic Pain?</h2>
                                    <p style="text-align:justify">It is a pain in the area below the belly button and between the hips that lasts six months or longer.</p>
                                    <h2>What are the symptoms of Chronic Pelvic Pain?</h2>
                                    <ul>
                                       <li>Severe and steady pain</li>
                                       <li>Pain that comes and goes</li>
                                       <li>Dull aching</li>
                                       <li>Sharp pains or cramping</li>
                                       <li>Heaviness deep within your pelvis</li>
                                       <li>Pain during sex</li>
                                       <li>Pain while having a bowel movement or urinating</li>
                                       <li>Pain when you sit for long periods</li>
                                    </ul>
                                    <h2>What are the causes of Chronic Pelvic Pain?</h2>
                                    <p style="text-align:justify"><strong>Chronic pelvic pain is a complex condition that can have a single cause or multiple causes. Some causes of chronic pelvic pain include:</strong></p>
                                    <ul>
                                       <li>Endometriosis</li>
                                       <li>Musculoskeletal problems</li>
                                       <li>Chronic pelvic inflammatory disease</li>
                                       <li>Ovarian remnant</li>
                                       <li>Fibroids</li>
                                       <li>Irritable bowel syndrome</li>
                                       <li>Painful bladder syndrome</li>
                                       <li>Pelvic congestion syndrome</li>
                                       <li>Psychological factors</li>
                                    </ul>
                                    <h2>What is the diagnosis of Chronic Pelvic Pain?</h2>
                                    <p style="text-align:justify">The doctor will suggest the following test or exam:</p>
                                    <p style="text-align:justify"><b>Pelvic exam: </b>It is done to check for any infection, abnormal growths or tense pelvic floor muscles.</p>
                                    <p style="text-align:justify"><b>Lab tests: </b>They are done to check for infections such as Chlamydia or Gonorrhoea.</p>
                                    <p style="text-align:justify"><b>Ultrasound:</b> It is done to detect masses or cysts in the ovaries, uterus or fallopian tubes.</p>
                                    <p style="text-align:justify"><b>Other imaging tests:</b> The doctor may recommend abdominal X-rays, CT-scans, or MRI to detect abnormal structures or growths.</p>
                                    <p style="text-align:justify"><b>Laparoscopy:</b> It is a surgical procedure in which a small incision is made in the abdomen and examined with a laparoscope.</p>
                                    <h2>How is Chronic Pelvic Pain Treated?M</h2>
                                    <p style="text-align:justify">The treatment is aimed to reduce symptoms and improve quality of life. Depending on the cause, the doctor may recommend several medications to treat the condition such as pain killers, hormone treatment, antibiotics, and antidepressants.</p>
                                    <p style="text-align:justify">The doctor may also recommend specific procedures for the treatment of chronic pelvic pain. These may include:</p>
                                    <p style="text-align:justify"><b>Physical therapy:</b> Exercises, massage, and other relaxation techniques may improve chronic pelvic pain.</p>
                                    <p style="text-align:justify"><b>Neuro-stimulation:</b> It involves implanting a device that blocks nerve pathways so that the pain signal cannot reach the brain.</p>
                                    <p style="text-align:justify"><b>Trigger point injections: </b>In this method, analgesic medicine is injected into the painful areas to block pain and ease discomfort.</p>
                                    <p style="text-align:justify"><b>Surgical Procedure:</b> To correct the underlying problem that causes chronic pelvic pain, the doctor may recommend a surgical procedure such as Laparoscopic Surgery and Hysterectomy.</p>
                                   
