                                    <h3>Overview</h3>
                                    <p style="text-align:justify"></p>
                                    <p style="text-align:justify">It is quite important for women to pay attention to their reproductive health. It is also said that a happy vagina makes a happy woman! Therefore, it is vital to maintain the ecosystem down there.</p>
                                    <p style="text-align:justify">Thanks to a healthy combination of bacteria and the perfectly poised pH levels that discourage the harmful microbes from growing. But this combination can be easily tampered by unhealthy hygiene practices, infections, medical conditions and certain medications.</p>
                                    <p style="text-align:justify">Therefore, while maintaining those healthy habits, you need to get yourself regularly checked by a good gynaecologist for the sake of your reproductive health.</p>
                                    <h2>Who is a gynaecologist?</h2>
                                    <p style="text-align:justify">Gynaecologists are doctors who specialize in women’s health, particularly the female reproductive system. They deal with female problems related to fertility, menstruation, sexually transmitted infections (STIs), and hormone disorders, among others. They also deal with female issues including obstetrics or pregnancy and childbirth.</p>
                                    <p style="text-align:justify">A gynaecologist must have an educational background of at least 8 years (trained as a doctor for 4 years plus 4 years of specialization in the field of obstetrics and gynaecology). Only after passing an examination, they will be able to certify and register themselves as a gynaecologist.</p>
                                    <h2>Why do you need to visit a gynaecologist?</h2>
                                    <p style="text-align:justify"><strong>You need to visit a gynaecologist in case if you are facing any gynaecological symptoms given below, like:</strong></p>
                                    <ul>
                                       <li>Irregular periods</li>
                                       <li>Chronic pelvic pain</li>
                                       <li>Painful intercourse</li>
                                       <li>Bleeding while having intercourse</li>
                                       <li>Lumps or bumps in the vaginal region</li>
                                       <li>Infertility</li>
                                       <li>Irregular discharge</li>
                                    </ul>
                                    <p style="text-align:justify">The above-mentioned symptoms may not seem very serious, but they may be a sign of some severe underlying condition. That is why you should consult with a gynaecologist at the earliest. Depending on the condition of the patient the gynaecologist will offer the diagnosis and treatment like an ultrasound scan, hysteroscopy or a colposcopy.</p>
                                    <p style="text-align:justify"><strong>Conditions commonly treated by gynaecologists are:</strong></p>
                                    <ul>
                                       <li>Problems relating to pregnancy, fertility, menstruation and menopause</li>
                                       <li>STIs &amp; STDs</li>
                                       <li>PCOS (polycystic ovary syndrome)</li>
                                       <li>PID (pelvic inflammatory disease)</li>
                                       <li>UTIs</li>
                                       <li>Endometrioses and other chronic conditions</li>
                                       <li>Urinary and foetal incontinence</li>
                                       <li>Pelvic floor dysfunction</li>
                                       <li>Pregnancy termination (lawful)</li>
                                       <li>Benign and malignant tumours in the female reproductive organs</li>
                                       <li>Premalignant conditions like endometrial hyperplasia, and cervical dysplasia</li>
                                       <li>Congenital abnormalities (in the female reproductive tract)</li>
                                    </ul>
