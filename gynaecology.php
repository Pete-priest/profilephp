                                    <h3>Fibroids</h3>
                                    <p style="text-align:justify">Fibroids are abnormal, non-cancerous growths that develop in or on the uterus. The cause behind these growths or tumours is unknown and sometimes fibroids don’t cause any symptoms, so in most cases, women don’t even know that they have them.</p>
                                    <h2>Causes and risk factors M</h2>
                                    <p style="text-align:justify"><strong>Although the cause behind fibroids is unclear, certain factors influence their development. </strong></p>
                                    <ul>
                                       <li>Family history of the condition increases the chances of their development.</li>
                                       <li>Hormones oestrogen and progesterone may stimulate their growth. So, they can occur during a menstrual cycle or during pregnancy.</li>
                                       <li>Women who are on the heavier body weight side and those who are above the age of 30 are at higher risk.</li>
                                    </ul>
                                    <h2>Types of fibroids</h2>
                                    <h2><strong>Fibroids are classified based on where they develop on the uterus:</strong></h2>
                                    <ul>
                                       <li>The most common type is intramural fibroids that develop in the muscle layer of the uterus.</li>
                                       <li>Subserosal fibroids develop on the outside of the uterus and can make the womb look larger on one side.</li>
                                       <li>When the subserosal fibroids develop a base and stem like structure to support the growth, they are called pedunculated fibroids.</li>
                                       <li>Fibroids that develop in the middle mucosa of the uterus are called submucosal fibroids.</li>
                                    </ul>
                                    <h2>What are the symptoms of fibroids?</h2>
                                    <p style="text-align:justify">The symptoms of fibroids depend upon the location and size of the growth. If the fibroid is small, you may not experience any symptoms while submucosal fibroids could cause heavy bleeding and trouble in conceiving. Possible symptoms include:</p>
                                    <ul>
                                       <li>Heavy bleeding during periods and otherwise</li>
                                       <li>Pelvic pain</li>
                                       <li>Menstrual cramping</li>
                                       <li>Abdominal bloating or swelling</li>
                                       <li>Pain while having intercourse</li>
                                       <li>Longer menstrual cycles</li>
                                    </ul>
                                    <h2>Diagnosis and treatment of fibroids</h2>
                                    <p style="text-align:justify">During a regular pelvic exam, your gynaecologist will check your uterus to look for any abnormalities. You may also be required to get an ultrasound or MRI scan done so that the doctor can get a clear picture of the location and size of the fibroid.</p>
                                    <p style="text-align:justify">The treatment plan is developed by your doctor, keeping in mind various factors like your age, health, size and location of the fibroid. Some dietary and lifestyle changes may be recommended, medications to help shrink fibroids and other options to help manage the pain and bleeding. In cases where there are multiple growths or the fibroids are very large, surgical procedures like a laparoscopic surgery may be required to remove the fibr</p>
