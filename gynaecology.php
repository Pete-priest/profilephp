                                    <h3 class="h3-md steelblue-color">Vaginal Yeast Infection</h3>
									
                  <h3></h3>
                          <p style="text-align:justify">Yeast is a fungus that is commonly found living in the vagina in small numbers. The Lactobacillus acidophilus bacteria are responsible for keeping the yeast count under check. When something disrupts this balance, the yeast cells grow excessively, causing an infection. Vaginal yeast infection is a result of excessive growth of the yeast cells in the vagina which may be triggered by various factors like certain antibiotics which cause an imbalance, high levels of oestrogen which may be due to pregnancy or hormone therapy or sometimes health conditions like HIV or diabetes.</p>
<h2>What are the symptoms of a yeast infection?</h2>
<p style="text-align:justify">Vaginal yeast infections can cause a lot of worry but are mostly never serious. These infections are quite common, easy to treat and symptoms include:</p>
<ul>
<li>Vaginal itching and soreness of the vagina</li>
<li>Pain or burning sensation during urination</li>
<li>Thick white vaginal discharge</li>
</ul>
<h2>Vaginal Yeast Infection Treatment</h2>
<p style="text-align:justify">These infections are common and easy to diagnose on your own, but if you are having doubts it is best to consult your gynaecologist. The symptoms are more common just before your periods and the infection is common around pregnancy too. During pregnancy or even otherwise, it is best to see if the infection clears up on its own over a period. In case it doesn’t clear, your doctor will prescribe some simple antifungal creams or tablets to help.</p>

  
